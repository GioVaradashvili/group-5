def triangle_perimeter(side1, side2, side3):
    perimeter = side1 + side2 + side3
    print("Triangle Perimeter:", perimeter)

# Example usage
side1 = float(input("შეიყვანეთ პირველი მხარის სიგრძე: "))
side2 = float(input("შეიყვანეთ მეორე მხარის სიგრძე: "))
side3 = float(input("შეიყვანეთ მესამე მხარის სიგრძე: "))
triangle_perimeter(side1, side2, side3)